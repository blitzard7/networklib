namespace NetworkLib
{
    /// <summary>
    ///     Represents the <see cref="ResponseType"/> enumeration.
    /// </summary>
    public enum ResponseType
    {
        /// <summary>
        ///     Represents the connect type.
        /// </summary>
        Connect,

        /// <summary>
        ///     Represents the disconnect type.
        /// </summary>
        Disconnect,

        /// <summary>
        ///     Represents the is alive type.
        /// </summary>
        IsAlive,

        /// <summary>
        ///     Represents the send type.
        /// </summary>
        Send
    }
}