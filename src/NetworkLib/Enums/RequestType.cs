namespace NetworkLib
{
    /// <summary>
    ///     Represents the <see cref="RequestType"/> enumeration.
    /// </summary>
    public enum RequestType
    {
        /// <summary>
        ///     Represents the connect type.
        /// </summary>
        Connect,

        /// <summary>
        ///     Represents the send data type.
        /// </summary>
        SendData,

        /// <summary>
        ///     Represents the is alive type.
        /// </summary>
        IsAlive,

        /// <summary>
        ///     Represents the disconnect type.
        /// </summary>
        Disconnect
    }
}